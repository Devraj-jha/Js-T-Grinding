// string – Represents a sequence of characters, e.g., "hello"

// number – Represents all numeric values, including integers and decimals, e.g., 42, 3.14

// boolean – Represents true or false, used for logical decisions

// undefined – A variable that has been declared but not assigned a value

// null – Represents the intentional absence of any object value

// symbol – Represents a unique and immutable identifier, often used as object keys

// bigint – Represents integers larger than 2^53 - 1, written with n at the end, e.g., 12345678901234567890n